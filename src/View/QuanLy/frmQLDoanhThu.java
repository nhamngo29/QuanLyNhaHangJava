    private void Tong() {
        DecimalFormat x = new DecimalFormat("###,###,###");
        int Tong = 0;
        for (int i = 0; i < tbHoaDon.getRowCount(); i++) {
            Object obj = tbHoaDon.getValueAt(i, 4);
            if (obj != null && obj instanceof Number) {
                Tong += ((Number) obj).intValue();
            }
        }
        txtTong.setText(x.format(Tong) + " VNĐ");
    }
