    private void txtSoLuongFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtSoLuongFocusLost
        // TODO add your handling code here:
        int sl=0;
        try {
            sl=Integer.parseInt(txtSoLuong.getText().toString());
            if(sl<=0||sl>a.getSoLuong())
            {
                
                new MyDialog("Số lượng phải nhỏ hơn số lượng tốn hàng hoặc lớn hơn 0 vui lòng kiểm tra lại", MyDialog.ERROR);
                txtSoLuong.setText(""+a.getSoLuong());
            }
            dao.insertOrUpdate(a);
        } catch (Exception e) {
            new MyDialog("Vui lòng nhập số", MyDialog.ERROR);
            txtSoLuong.setText(""+a.getSoLuong());
            
        }
    }//GEN-LAST:event_txtSoLuongFocusLost

